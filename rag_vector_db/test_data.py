def save_test_documents(output_dir="data/raw"):
    """Сохранение тестовых документов в файлы"""
    import os
    from pathlib import Path

    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    saved_files = []
    for doc in TEST_DOCUMENTS:
        filename = f"{doc['type']}_{doc['title'].lower().replace(' ', '_')}.txt"
        filepath = output_path / filename

        content = f"# {doc['title']}\n\n{doc['content']}\n\nТэги: {', '.join(doc['tags'])}"

        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)

        saved_files.append(filepath)
        print(f"Создан файл: {filepath}")

    print(f"\n✅ Создано {len(saved_files)} тестовых документов в {output_dir}")
    return saved_files


